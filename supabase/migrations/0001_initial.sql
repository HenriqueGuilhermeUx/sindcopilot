create or replace function public.consume_monthly_usage(
  p_user_id uuid,
  p_metric text,
  p_limit integer,
  p_amount integer default 1
) returns boolean
language plpgsql security definer set search_path = public as $$
declare
  v_period date := date_trunc('month', now())::date;
  v_current integer;
begin
  insert into public.usage_monthly(user_id, period_start)
  values (p_user_id, v_period)
  on conflict (user_id, period_start) do nothing;

  if p_metric = 'ocr' then
    select ocr_count into v_current from public.usage_monthly where user_id=p_user_id and period_start=v_period for update;
    if v_current + p_amount > p_limit then return false; end if;
    update public.usage_monthly set ocr_count=ocr_count+p_amount, updated_at=now() where user_id=p_user_id and period_start=v_period;
  elsif p_metric = 'ai' then
    select ai_count into v_current from public.usage_monthly where user_id=p_user_id and period_start=v_period for update;
    if v_current + p_amount > p_limit then return false; end if;
    update public.usage_monthly set ai_count=ai_count+p_amount, updated_at=now() where user_id=p_user_id and period_start=v_period;
  elsif p_metric = 'notices' then
    select notices_count into v_current from public.usage_monthly where user_id=p_user_id and period_start=v_period for update;
    if v_current + p_amount > p_limit then return false; end if;
    update public.usage_monthly set notices_count=notices_count+p_amount, updated_at=now() where user_id=p_user_id and period_start=v_period;
  else
    raise exception 'Métrica inválida';
  end if;
  return true;
end $$;

create or replace function public.reserve_storage(
  p_user_id uuid,
  p_limit_bytes bigint,
  p_amount_bytes bigint
) returns boolean
language plpgsql security definer set search_path = public as $$
declare v_current bigint;
begin
  select storage_bytes into v_current from public.users where id=p_user_id for update;
  if coalesce(v_current,0) + p_amount_bytes > p_limit_bytes then return false; end if;
  update public.users set storage_bytes=storage_bytes+p_amount_bytes where id=p_user_id;
  return true;
end $$;

create or replace function public.release_storage(p_user_id uuid, p_amount_bytes bigint)
returns void language sql security definer set search_path=public as $$
  update public.users
  set storage_bytes = greatest(0, storage_bytes - greatest(0, p_amount_bytes))
  where id=p_user_id;
$$;

create or replace function public.match_document_chunks(
  query_embedding vector(1536),
  match_user_id uuid,
  match_condominium_id bigint,
  match_count integer default 8
) returns table (
  id bigint,
  document_id bigint,
  title text,
  page_number integer,
  content text,
  similarity double precision
)
language sql stable as $$
  select dc.id, dc.document_id, d.title, dc.page_number, dc.content,
         1 - (dc.embedding <=> query_embedding) as similarity
  from public.document_chunks dc
  join public.documents d on d.id = dc.document_id
  where dc.user_id = match_user_id
    and dc.condominium_id = match_condominium_id
  order by dc.embedding <=> query_embedding
  limit match_count;
$$;

