create or replace function public.handle_new_auth_user()
returns trigger language plpgsql security definer set search_path = public as $$
begin
  insert into public.users (
    id, email, name, terms_accepted_at, lgpd_consent_at, lgpd_consent_version
  )
  values (
    new.id,
    new.email,
    coalesce(new.raw_user_meta_data->>'name', split_part(coalesce(new.email,''), '@', 1)),
    case when new.raw_user_meta_data->>'accepted_terms' = 'true' then now() else null end,
    case when new.raw_user_meta_data->>'accepted_terms' = 'true' then now() else null end,
    case when new.raw_user_meta_data->>'accepted_terms' = 'true' then coalesce(new.raw_user_meta_data->>'lgpd_version','1.0') else null end
  )
  on conflict (id) do nothing;
  return new;
end $$;
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created after insert on auth.users for each row execute function public.handle_new_auth_user();

